print(X.head(3))

"""### Penjelasan Cell: Pembersihan dan Pemisahan Data

Cell ini melakukan pra-pemrosesan data dengan menghapus kolom yang tidak relevan, kemudian memisahkan fitur untuk klustering (`X`) dari data demografi (`demo`). Ini penting untuk memastikan data siap digunakan dalam analisis klustering dan untuk interpretasi hasil demografi selanjutnya.
"""

#===== Normalisasi menggunakan Standard Scaling (Z-score) =====#


