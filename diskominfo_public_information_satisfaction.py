"""### Penjelasan Cell: Pemuatan dan Penamaan Ulang Data

Cell ini bertujuan untuk memuat dataset responden dari Google Drive ke dalam DataFrame. Setelah dimuat, nama-nama kolom disesuaikan agar lebih deskriptif dan mudah dipahami untuk analisis selanjutnya. Pada akhirnya, cell ini juga menampilkan dimensi data dan beberapa baris pertama untuk pratinjau cepat.
"""

#===== CLEANING & DROP =====#

# Kolom yang tidak relevan sama sekali
drop_cols = ['Nama', 'Kontak', 'Waktu']
df = df.drop(columns=drop_cols)

# Pisahkan fitur clustering dan demografi
q_cols = ['Q1','Q2','Q3','Q4','Q5','Q6','Q7','Q8','Q9','Q10','Q11','Q12','Q13','Q14','Q15']
