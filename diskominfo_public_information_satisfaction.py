#===== Clustering K-Means =====#


inertias = []
sil_scores = []
K_range = range(2, 9)

for k in K_range:
    km = KMeans(n_clusters=k, random_state=42, n_init=10)
    km.fit(X_scaled)
    inertias.append(km.inertia_)
    sil_scores.append(silhouette_score(X_scaled, km.labels_))

print("K | Inertia | Silhouette")
for k, inertia, sil in zip(K_range, inertias, sil_scores):
    print(f"{k} | {inertia:.2f}  | {sil:.3f}")

"""### Penjelasan Cell: Klustering K-Means - Penentuan Jumlah Cluster Optimal

Cell ini bertujuan untuk menentukan jumlah cluster (K) yang paling optimal untuk algoritma K-Means. Proses ini melibatkan iterasi K-Means dengan berbagai jumlah cluster (`K_range`) dan evaluasi hasilnya menggunakan metrik *Inertia* (metode Elbow) dan *Silhouette Score*. Hasil berupa tabel `K`, `Inertia`, dan `Silhouette Score` disajikan untuk membantu dalam penentuan K optimal.
"""

#===== visualisasikan =====#

fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(12, 5))

# Elbow
ax1.plot(K_range, inertias, 'o-', color='steelblue', linewidth=2, markersize=7)
ax1.axvline(x=3, color='red', linestyle='--', label='K optimal = 3')
ax1.set_title('Elbow Method', fontweight='bold')
ax1.set_xlabel('Jumlah Cluster (K)')
ax1.set_ylabel('Inertia')
ax1.legend()

# Silhouette
ax2.plot(K_range, sil_scores, 's-', color='green', linewidth=2, markersize=7)
ax2.axvline(x=3, color='red', linestyle='--', label='K optimal = 3')
ax2.set_title('Silhouette Score', fontweight='bold')
ax2.set_xlabel('Jumlah Cluster (K)')
ax2.set_ylabel('Silhouette Score')
ax2.legend()

plt.tight_layout()
plt.show()

"""### Penjelasan Cell: Visualisasi Metode Elbow dan Silhouette Score

Cell ini memvisualisasikan hasil dari evaluasi klustering K-Means menggunakan metode Elbow dan Silhouette Score. Plot `Inertia` dan `Silhouette Score` ditampilkan untuk berbagai nilai K, dengan indikasi `K=3` sebagai jumlah cluster optimal. `K=3` dipilih berdasarkan titik 'siku' yang jelas pada metode Elbow dan nilai *Silhouette Score* yang relatif tinggi, menunjukkan struktur cluster yang baik.
"""

#===== Final Clustering dengan K=3 =====#

km_final = KMeans(n_clusters=3, random_state=42, n_init=10)
